class TestMutationPropagation:
    def test_update_decision_name_propagates_to_scenario(self, vm_sas: object) -> None:
        """update_decision_name marks dirty and updates scenario.decisions in-place."""
        scenario_before = vm_sas.scenario  # type: ignore[attr-defined]
        first_decision = scenario_before.decisions[0]
        old_name = first_decision.name

        vm_sas.update_decision_name(first_decision.id, "Renamed Decision")  # type: ignore[attr-defined]

        assert vm_sas.scenario is not None  # type: ignore[attr-defined]
        updated = next(d for d in vm_sas.scenario.decisions if d.id == first_decision.id)  # type: ignore[attr-defined]
        assert updated.name == "Renamed Decision"
        assert vm_sas.is_dirty is True  # type: ignore[attr-defined]

        # Cleanup: restore
        vm_sas.update_decision_name(first_decision.id, old_name)  # type: ignore[attr-defined]

    def test_update_property_weight_propagates(self, vm_sas: object) -> None:
        """Changing property weight should propagate to scenario and re-solve."""
        first_prop = vm_sas.scenario.properties[0]  # type: ignore[attr-defined]
        old_weight = first_prop.weight

        vm_sas.update_property(first_prop.id, weight=old_weight * 10.0)  # type: ignore[attr-defined]

        # Scenario updated
        updated_prop = next(
            p
            for p in vm_sas.scenario.properties  # type: ignore[attr-defined]
            if p.id == first_prop.id
        )
        assert updated_prop.weight == pytest.approx(old_weight * 10.0)

    def test_add_decision_updates_scenario(self, vm_sas: object) -> None:
        count_before = len(vm_sas.scenario.decisions)  # type: ignore[attr-defined]
        new_id = vm_sas.add_decision("Test Decision X")  # type: ignore[attr-defined]
        assert len(vm_sas.scenario.decisions) == count_before + 1  # type: ignore[attr-defined]
        assert any(d.id == new_id for d in vm_sas.scenario.decisions)  # type: ignore[attr-defined]

    def test_delete_decision_cascades(self, vm_sas: object) -> None:
        # Add a decision then delete it and verify cascade
        new_id = vm_sas.add_decision("To Delete")  # type: ignore[attr-defined]
        alt_id = vm_sas.add_alternative(new_id, "Alt Under Deleted")  # type: ignore[attr-defined]

        vm_sas.delete_decision(new_id)  # type: ignore[attr-defined]

        assert not any(d.id == new_id for d in vm_sas.scenario.decisions)  # type: ignore[attr-defined]
        assert not any(a.id == alt_id for a in vm_sas.scenario.alternatives)  # type: ignore[attr-defined]


