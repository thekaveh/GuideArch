def _make_hub_and_dispatcher() -> tuple[Any, Any]:
    """Return a real MessageHub + RxDispatcher for child VM construction tests."""
    from vmx.services.dispatcher import RxDispatcher
    from vmx.services.message_hub import MessageHub

    hub: MessageHub = MessageHub()  # type: ignore[type-arg]
    dispatcher = RxDispatcher.immediate()
    return hub, dispatcher


def _has_property(instance: object, name: str) -> bool:
    klass = type(instance)
    return any(name in base.__dict__ for base in klass.__mro__)


