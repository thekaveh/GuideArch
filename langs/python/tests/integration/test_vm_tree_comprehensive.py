"""Comprehensive VM tree integration tests.

Walks every node in spec/viewmodels.md §2 and asserts:
  1. Existence — class importable from guidearch.viewmodels
  2. Construction — VM uses ComponentVMOf or is a plain class wrapper
  3. Observable properties — properties exist with correct types; emit
     PropertyChangedMessage when mutated (where applicable)
  4. Commands — present on ScenarioVM; execute() works; can_execute matches docs
  5. Mutation propagation — child VM mutation propagates to parent scenario
  6. Solve trigger semantics — parameterized matrix per spec §3.3
  7. Result VMs are read-only — no mutation methods

Deviation notes
---------------
- CompositeVM wrappers (DecisionsVM, PropertiesVM, etc.) are NOT separate
  classes in the current implementation; the VM tree is managed directly on
  ScenarioVM.  Each of those conceptual "aggregate" types is realised via
  Python lists/tuples on the ScenarioVM, which is documented here.
- AlternativesVM is similarly folded in; alternatives are nested under decisions
  in the scenario model and accessed via ScenarioVM.scenario.alternatives.
- AnalysisVM (M4) is not yet a separate class; chart data is generated by
  guidearch.view.chart_data helper functions.
"""

